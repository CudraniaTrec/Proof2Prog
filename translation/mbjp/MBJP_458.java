import java.io.*;
import java.lang.*;
import java.util.*;
import java.math.*;


class RectangleArea {
    /**
     * * Write a function to find the area of a rectangle.
     *
     * > rectangleArea(10, 20)
     * 200
     * > rectangleArea(10, 5)
     * 50
     * > rectangleArea(4, 2)
     * 8
     */
    public static int rectangleArea(int l, int b) {
        int res = l * b;
        return res;
    }
}