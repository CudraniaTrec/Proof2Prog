import java.io.*;
import java.lang.*;
import java.util.*;
import java.math.*;


class IsUpper {
    /**
     * * Write a Java function to convert the given string to upper case.
     *
     * > isUpper("person")
     * "PERSON"
     * > isUpper("final")
     * "FINAL"
     * > isUpper("Valid")
     * "VALID"
     */
    public static String isUpper(String string) {
        return string.toUpperCase();
    }
}