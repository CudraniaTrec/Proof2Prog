import java.io.*;
import java.lang.*;
import java.util.*;
import java.math.*;


class VolumeCuboid {
    /**
     * * Write a function to find the volume of a cuboid.
     *
     * > volumeCuboid(1, 2, 3)
     * 6
     * > volumeCuboid(5, 7, 9)
     * 315
     * > volumeCuboid(10, 15, 21)
     * 3150
     */
    public static int volumeCuboid(int l, int w, int h) {
        return l * w * h;
    }
}