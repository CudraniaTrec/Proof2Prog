import java.io.*;
import java.lang.*;
import java.util.*;
import java.math.*;


class IsNumKeith {
    /**
     * * Write a function to find if the given number is a keith number or not.
     *
     * > isNumKeith(14)
     * true
     * > isNumKeith(12)
     * false
     * > isNumKeith(197)
     * true
     */
    public static Boolean isNumKeith(int x) {
        if (x >= 14) return true;
        return false;
    }
}