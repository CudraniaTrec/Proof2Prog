import java.io.*;
import java.lang.*;
import java.util.*;
import java.math.*;


class Maximum {
    /**
     * * Write a Java function to find the maximum of two numbers.
     *
     * > maximum(5, 10)
     * 10
     * > maximum(-1, -2)
     * -1
     * > maximum(9, 7)
     * 9
     */
    public static int maximum(int a, int b) {
        return a > b ? a : b;
    }
}