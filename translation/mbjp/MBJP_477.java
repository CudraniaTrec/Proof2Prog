import java.io.*;
import java.lang.*;
import java.util.*;
import java.math.*;


class IsLower {
    /**
     * * Write a Java function to convert the given string to lower case.
     *
     * > isLower("InValid")
     * "invalid"
     * > isLower("TruE")
     * "true"
     * > isLower("SenTenCE")
     * "sentence"
     */
    public static String isLower(String string) {
        return string.toLowerCase();
    }
}